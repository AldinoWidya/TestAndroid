package com.example.testandroid.Model;

public class DataModel {
    private int id;
    private String no_laporan, nik_pelapor, nama_pelapor, tanggal_lapor, tempat;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNo_laporan() {
        return no_laporan;
    }

    public void setNo_laporan(String no_laporan) {
        this.no_laporan = no_laporan;
    }

    public String getNik_pelapor() {
        return nik_pelapor;
    }

    public void setNik_pelapor(String nik_pelapor) {
        this.nik_pelapor = nik_pelapor;
    }

    public String getNama_pelapor() {
        return nama_pelapor;
    }

    public void setNama_pelapor(String nama_pelapor) {
        this.nama_pelapor = nama_pelapor;
    }

    public String getTanggal_lapor() {
        return tanggal_lapor;
    }

    public void setTanggal_lapor(String tanggal_lapor) {
        this.tanggal_lapor = tanggal_lapor;
    }

    public String getTempat() {
        return tempat;
    }

    public void setTempat(String tempat) {
        this.tempat = tempat;
    }

}
